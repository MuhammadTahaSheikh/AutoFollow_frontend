export default function LeadRowActions({
  leadId,
  onAiFollowUp,
  onEdit,
  onDelete,
  canDelete,
}: LeadRowActionsProps) {
  return (
    <div className="inline-flex items-center gap-0.5 whitespace-nowrap">
      <IconButton href={`/dashboard/leads/${leadId}`} title="View lead" variant="brand">
        <ViewIcon />
      </IconButton>
      <IconButton title="AI Follow-up" onClick={onAiFollowUp}>
        <SparklesIcon />
      </IconButton>
      <IconButton title="Edit lead" onClick={onEdit}>
        <PencilIcon />
      </IconButton>
      {canDelete && onDelete && (
        <IconButton title="Delete lead" onClick={onDelete} variant="danger">
          <TrashIcon />
        </IconButton>
      )}
    </div>
  );
}
