'use client';

import Link from 'next/link';

interface LeadRowActionsProps {
  leadId: number;
  onAiFollowUp: () => void;
  onEdit: () => void;
  onDelete?: () => void;
  canDelete?: boolean;
}

function IconButton({
  children,
  title,
  onClick,
  href,
  variant = 'default',
}: {
  children: React.ReactNode;
  title: string;
  onClick?: () => void;
  href?: string;
  variant?: 'default' | 'danger' | 'brand';
}) {
  const className = `inline-flex items-center justify-center w-8 h-8 rounded-lg transition-colors ${
    variant === 'danger'
      ? 'text-slate-400 hover:text-red-600 hover:bg-red-50'
      : variant === 'brand'
        ? 'text-brand-600 hover:text-brand-700 hover:bg-brand-50'
        : 'text-slate-500 hover:text-slate-700 hover:bg-slate-100'
  }`;

  if (href) {
    return (
      <Link href={href} title={title} className={className}>
        {children}
      </Link>
    );
  }

  return (
    <button type="button" title={title} onClick={onClick} className={className}>
      {children}
    </button>
  );
}

